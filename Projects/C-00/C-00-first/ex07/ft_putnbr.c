void ft_putnbr(int nb)
{
	char	c;

	while(nb > 10)
	{
		c = (nb % 10) + '0';
		write(1, &c, 1);
		nb /= 10;
	} 
	c = nb + '0';
	write(1, &c, 1);
}

int	main()
{
	ft_putnbr(4256);
	return (0);
}
