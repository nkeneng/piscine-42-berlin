#include <unistd.h>

void	trim_spaces(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0' && str[i] == ' ')
	{
		i++;
	}
	while (str[i] != '\0')
	{
		if (str[i] == ' ')
		{
			while (str[i] == ' ')
				i++;
		}
		write(1, &str[i], 1);
		i++;
	}
}
